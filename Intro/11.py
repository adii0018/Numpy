import numpy as np

arr = np.array([10, 20, 30, 40, 50])

# Mean
print("Mean:", np.mean(arr))

# Median
print("Median:", np.median(arr))

# Standard Deviation
print("Std:", np.std(arr))

# Variance
print("Variance:", np.var(arr))

# Min & Max
print("Min:", np.min(arr))
print("Max:", np.max(arr))

# Sum
print("Sum:", np.sum(arr))